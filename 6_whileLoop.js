//* WHILE LOOP
console.log("WHILE LOOP");
let i = 0;
while (i < 10) {
  console.log(i);
  i++;
}
//! works from initial of i and to (n-1)...here from 0 to 9

//* DO-WHILE LOOP
console.log("DO WHILE LOOP");
let j = 0;
do {
  console.log(j);
  j++;
} while (j < 10);

//! do while loop runs altest 1 time [Put i and j as 11 to see].