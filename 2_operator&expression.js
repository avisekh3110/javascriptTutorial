// Operators - Arithmatic operator & assingment operator
// Arithmatic operator
//! "let" has a block level scope whereas "var" has global level scope.
let a = 10;
let b = 5;
console.log("a+b", a + b);
console.log("a-b", a - b);
console.log("a*b", a * b);
console.log("a/b", a / b);
console.log("a**b", a ** b);
console.log("a%b", a % b);
console.log("a++", a++); //a++ => first print the value of a and then changes.
console.log("a", a);
console.log("++a", ++a); //++a => first changes the value of a and then print.
console.log("a--", a--);
console.log("a", a);
console.log("--a", --a);

// conditional operator
let num1 = 6;
let num2 = "6";
console.log(num1 == num2); //Checks is num1 = num2
console.log(num1 != num2);
console.log(num1 === num2); //Checks is num1 = num2 and also type of num1 = num2
console.log(num1 !== num2);

